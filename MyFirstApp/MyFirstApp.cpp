// This is a header file or a library
// iostream is used to take input or return output 
#include <iostream>

int main()
{
    // std is the namespace for cout
    // This will output to the console Hello World!
    std::cout << "Hello World!\n";
    // endl is a substitute for '\n'
    std::cout << "My name is Dhruv" << std::endl; 
    
    // This is to remove the junk text at the end of the execution
    system("pause>0");

    // Need to put ';' at the end of each line to indicate EOL

    // If you wish to run the file without having to open Visual Studio, you can 
    // Go to the Debug folder and double click the .exe file
    // Note: This will only run the last built app
}
